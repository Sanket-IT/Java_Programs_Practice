public class SwitchCaseCharacter{
public static void main (String args[]){
char ch='Z';
switch(ch)
{
 case 'A':
System.out.println("Case A:Charcter is A");
break;
case 'B':
System.out.println("Case B:Character is B");
break;
case 'C':
System.out.println("Case C:Character is C");
break;
default:
System.out.println("Default:Character is"+ch);
}
}
}